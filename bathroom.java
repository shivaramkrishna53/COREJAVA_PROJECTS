
public class bathroom {
int len,breath,height;

public bathroom(int len, int breath, int height) {
	super();
	this.len = len;
	this.breath = breath;
	this.height = height;
}

public int getLen() {
	return len;
}

public void setLen(int len) {
	this.len = len;
}

public int getBreath() {
	return breath;
}

public void setBreath(int breath) {
	this.breath = breath;
}

public int getHeight() {
	return height;
}

public void setHeight(int height) {
	this.height = height;
}

}
