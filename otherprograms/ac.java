
public class ac {
private int capacity,price;
private String name;
public int getCapacity() {
	return capacity;
}
public void setCapacity(int capacity) {
	this.capacity = capacity;
}
public int getPrice() {
	return price;
}
public void setPrice(int price) {
	this.price = price;
}
public ac(int capacity, int price, String name) {
	super();
	this.capacity = capacity;
	this.price = price;
	this.name = name;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
} 

}
